#[cfg(feature = "bearssl-aes")]
#[test]
fn test_bearssl_aes_known_vectors() {
    // NIST FIPS-197 test vector
    let key = [
        0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E,
        0x0F, 0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1A, 0x1B, 0x1C, 0x1D,
        0x1E, 0x1F,
    ];
    let input = [
        0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xAA, 0xBB, 0xCC, 0xDD, 0xEE,
        0xFF,
    ];
    let expected = [
        0x8E, 0xA2, 0xB7, 0xCA, 0x51, 0x67, 0x45, 0xBF, 0xEA, 0xFC, 0x49, 0x90, 0x4B, 0x49, 0x60,
        0x89,
    ];

    let ctx = Aes256CtxPure::new(&key);
    let result = ctx.encrypt_block(&input);
    assert_eq!(
        result, expected,
        "BearSSL AES should match NIST test vector"
    );
    println!("✅ BearSSL AES passes NIST FIPS-197 test vector");
}

