//! HQC hash_i KAT and SHA3 determinism tests.
//!
//! The main test validates **hash_i** (HQC keygen seed derivation: SHA3-512(seed_pke || 2))
//! against the **HQC reference** intermediate values for count=0.
//! Source of truth: `lib-q-hqc/kats/ref/hqc-1/intermediates_values` (seed_pke, seed_dk, seed_ek).
//! Passing the main test means our hash_i (and thus lib_q_sha3 for this input) matches the reference.

use lib_q_hqc::Hqc1Params;
use lib_q_hqc::hqc_pke::HqcPke;

/// HQC hash_i KAT: pke.hash_i(seed_pke) must match reference seed_dk and seed_ek.
/// Uses seed_pke and expected values from kats/ref/hqc-1/intermediates_values.
#[test]
fn test_hash_i_kat_against_reference() {
    // seed_pke from HQC reference intermediates (count=0)
    let seed_pke: [u8; 32] = [
        0x81, 0x31, 0x3D, 0xE3, 0x2A, 0xD3, 0x6C, 0x47, 0x79, 0x86, 0x5F, 0xE6, 0x6D, 0xDA, 0x28,
        0xAA, 0x92, 0x28, 0x81, 0x8C, 0x0F, 0x3E, 0x2F, 0xA0, 0x34, 0x8E, 0xF1, 0x6E, 0x37, 0x7D,
        0x10, 0x49,
    ];

    // Expected seed_dk and seed_ek from HQC reference intermediates
    let expected_seed_dk: [u8; 32] = [
        0x12, 0xDA, 0xF0, 0x31, 0xBD, 0xC7, 0xFC, 0x59, 0x2E, 0x00, 0x03, 0xA2, 0x1E, 0xEF, 0xA9,
        0xA1, 0x01, 0x95, 0x39, 0xAB, 0xCC, 0xC8, 0xF6, 0x70, 0x75, 0x94, 0x7C, 0xBF, 0xEA, 0xAC,
        0x98, 0xC5,
    ];
    let expected_seed_ek: [u8; 32] = [
        0xEF, 0x2B, 0x80, 0xF4, 0x6F, 0x3A, 0x64, 0x37, 0xB4, 0xD8, 0x69, 0xBB, 0x38, 0xBD, 0xD6,
        0x00, 0x4B, 0xFF, 0x72, 0xBC, 0xD0, 0xCE, 0xB1, 0x39, 0xB4, 0xB8, 0xD4, 0x73, 0x01, 0xF4,
        0xFC, 0xB1,
    ];

    let pke = HqcPke::<Hqc1Params>::new().expect("PKE creation failed");
    let mut output = [0u8; 64];
    pke.hash_i(&mut output, &seed_pke);

    assert_eq!(
        &output[..32],
        &expected_seed_dk[..],
        "hash_i seed_dk must match HQC reference intermediate value"
    );
    assert_eq!(
        &output[32..64],
        &expected_seed_ek[..],
        "hash_i seed_ek must match HQC reference intermediate value"
    );
}

/// Regression test: lib_q_sha3(seed_kem || 2) is deterministic.
/// This is *not* the HQC hash_i KAT (hash_i uses seed_pke, not seed_kem). It only ensures
/// our SHA3-512 output for this input remains stable across changes.
#[test]
fn test_sha3_512_seed_kem_determinism() {
    use lib_q_sha3::{
        Digest,
        Sha3_512,
    };

    // seed_kem from reference intermediates (32 bytes); domain = I_DOMAIN_SEPARATOR
    let seed_kem: [u8; 32] = [
        0x9E, 0xF8, 0x77, 0xFD, 0xDB, 0xE8, 0x89, 0x1C, 0x6E, 0x4E, 0x79, 0xEA, 0xF0, 0x22, 0xE5,
        0x63, 0xDE, 0xFA, 0xCA, 0x6B, 0x15, 0x21, 0x61, 0xB9, 0xA4, 0x23, 0xE8, 0xFE, 0x96, 0xA4,
        0x03, 0xE7,
    ];
    let domain: u8 = 2;

    let expected_first_half: [u8; 32] = [
        0x9D, 0x62, 0xC7, 0xFD, 0x98, 0xCA, 0xA7, 0x2F, 0x66, 0xDE, 0xDD, 0x56, 0x9D, 0x48, 0x33,
        0xFF, 0xBB, 0x2E, 0xED, 0x11, 0xF2, 0x76, 0x99, 0x6E, 0xE9, 0x79, 0x5B, 0xF2, 0x16, 0x70,
        0xBA, 0xC2,
    ];
    let expected_second_half: [u8; 32] = [
        0x27, 0x30, 0x0A, 0x83, 0x68, 0x9C, 0x18, 0xB0, 0x49, 0x89, 0xA5, 0x74, 0x0A, 0x32, 0xE8,
        0xA4, 0xF1, 0x27, 0x5B, 0x0D, 0x5F, 0xDA, 0xC3, 0x87, 0x4C, 0x9B, 0xF3, 0x37, 0x13, 0x45,
        0xCA, 0x37,
    ];

    let mut hasher = Sha3_512::new();
    hasher.update(&seed_kem);
    hasher.update([domain]);
    let result = hasher.finalize();

    assert_eq!(
        &result[..32],
        &expected_first_half[..],
        "lib_q_sha3(seed_kem || 2) first 32 bytes must be stable"
    );
    assert_eq!(
        &result[32..64],
        &expected_second_half[..],
        "lib_q_sha3(seed_kem || 2) second 32 bytes must be stable"
    );
}
